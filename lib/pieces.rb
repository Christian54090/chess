class Piece
=begin
  On new,
    takes position and color
    x coordinate = first val of position - editable
    y coordinate = second val of position - editable

  On legal moves,
    takes x and y
    depending on color and position on board,
    checks legal moves
    if move is legal,
    pushes move into an array
    if move is not legal,
    raises an error
=end
end
